def add(a,b)
    if(a>=0 && b>=0)
    return a+b
    end

end
def substract(c,d)
    if(c>=0 && d>=0 )
    return c-d
    
    end
end
def sum(total)
    ary = []
    total = 0
    for i in ary
    total += ary[i]
    end
    
    return total
    
end
def multiply(e,f)
    result = e*f
    if (e==0 || f==0)
        return 0
    else return result
    end
end
def power(g,h)
    return g**h
end
